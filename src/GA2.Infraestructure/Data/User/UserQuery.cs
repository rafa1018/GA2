    public static class UserQuery
    {
        public const string QueryGetAllUser = @"SELECT US.*,TI.TID_CODIGO FROM USR_USERS US INNER JOIN TID_TIPO_IDENTIFICACION TI ON TI.TID_ID=US.TID_ID;";

        public const string QueryGetByUser = @"SELECT * FROM USR_USERS WHERE USR_ID=@USR_ID";

        public const string QueryInsertUser = @"INSERT INTO USR_USERS (USR_ID,USR_NOMBRE,USR_SEGUNDONOMBRE,USR_APELLIDO,USR_SEGUNDOAPELLIDO,USR_USERNAME,USR_PASSWORD,USR_IDENTIFICACION,TID_ID,USR_EMAIL,USR_CREADOPOR,USR_FECHACREACION,USR_ESTADO,ACTIVE_DIRECTORY)VALUES (@USR_ID,@USR_NOMBRE,@USR_SEGUNDONOMBRE,@USR_APELLIDO,@USR_SEGUNDOAPELLIDO,@USR_USERNAME,@USR_PASSWORD,@USR_IDENTIFICACION,@TID_ID,@USR_EMAIL,@USR_CREADOPOR,GETDATE(),@USR_ESTADO,@ACTIVE_DIRECTORY);" + QueryGetByUser;

        public const string QueryGetIdentiUser = @"SELECT * FROM USR_USERS WHERE TID_ID=@TID_ID AND USR_IDENTIFICACION = @USR_IDENTIFICACION;"; 

        public const string QueryUpdateUser = @"UPDATE USR_USERS SET 
       USR_NOMBRE= @USR_NOMBRE,USR_SEGUNDONOMBRE=@USR_SEGUNDONOMBRE,USR_APELLIDO =@USR_APELLIDO,USR_SEGUNDOAPELLIDO = @USR_SEGUNDOAPELLIDO,USR_USERNAME = @USR_USERNAME
      ,USR_IDENTIFICACION = @USR_IDENTIFICACION,TID_ID =@TID_ID,USR_EMAIL = @USR_EMAIL,USR_ESTADO =@USR_ESTADO,ACTIVE_DIRECTORY=@ACTIVE_DIRECTORY WHERE USR_ID=@USR_ID;" + QueryGetByUser;

        public const string QueryUpdateStateUser = @"UPDATE USERS SET STATE=@STATE WHERE ID=@ID;" + QueryGetByUser;

        public const string QueryDelete = "DELETE FROM USR_USERS WHERE USR_ID = @USR_ID; " + QueryGetByUser;

        public const string QueryValidLogin = @"SELECT ID, NAME, LASTNAME, USERNAME, PASSWORD, MODIFIEDBY, MODIFIEDON, CREATEBY, CREATEON
                                                FROM GA2.dbo.USERS WHERE PASSWORD=@PASSWORD AND USERNAME=@USERNAME OR USERNAME=@USERNAME;";

    }
